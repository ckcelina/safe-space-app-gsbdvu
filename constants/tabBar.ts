
/**
 * Tab Bar Constants
 * 
 * Shared constants for the floating tab bar to ensure consistent spacing
 * across all screens that need to account for the tab bar height.
 */

/**
 * TAB_BAR_SPACER
 * 
 * The amount of bottom padding/spacing needed for scrollable content
 * to ensure the last items are fully visible above the floating tab bar.
 * 
 * This value accounts for:
 * - The tab bar height (~80px)
 * - Additional safe spacing (~40px)
 * 
 * Total: 120px
 */
export const TAB_BAR_SPACER = 120;
